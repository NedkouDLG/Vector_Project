#include "Element.h"

void Element::setX(double x)
{
	this->x = x;
}

void Element::setY(double y)
{
	this->y = y;
}

void Element::setZ(double z)
{
	this->z = z;
}

double Element::getX() const
{
	return this->x;
}

double Element::getY() const
{
	return this->y;
}

double Element::getZ() const
{
	return this->z;
}
