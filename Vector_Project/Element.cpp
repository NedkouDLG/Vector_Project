#include "Element.h"

void Element::setX(int x)
{
	this->x = x;
}

void Element::setY(int y)
{
	this->y = y;
}

void Element::setZ(int z)
{
	this->z = z;
}

int Element::getX() const
{
	return this->x;
}

int Element::getY() const
{
	return this->y;
}

int Element::getZ() const
{
	return this->z;
}
